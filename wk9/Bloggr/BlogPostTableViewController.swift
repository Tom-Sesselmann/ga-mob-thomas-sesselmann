        // Return the number of sections.
        return 1
    }

    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete method implementation.
        // Return the number of rows in the section.
        
        return blogPosts.count
    }

    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("blogPostCell", forIndexPath: indexPath) as! UITableViewCell
        
        if let post = blogPosts[indexPath.row] as? PFObject {
            cell.textLabel!.text = post.valueForKey("title") as? String
            cell.detailTextLabel!.text = post.valueForKey("subtitle") as? String
        }

        return cell
    }


